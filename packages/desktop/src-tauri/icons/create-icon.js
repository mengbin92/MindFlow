/**
 * Create a simple placeholder PNG icon for development
 */

const fs = require('fs');

// Minimal valid PNG (1x1 transparent pixel)
const png = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk start
  0x00, 0x00, 0x00, 0x20, // width: 32
  0x00, 0x00, 0x00, 0x20, // height: 32
  0x08, 0x02, 0x00, 0x00, 0x00, // bit depth: 8, color type: 2 (RGB), etc.
  0x2C, 0x6B, 0x60, 0x9A, // CRC
  0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54, // IDAT chunk start
  0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00, 0x00, // image data (compressed)
  0x03, 0x01, 0x01, 0x00, 0x18, 0xDD, 0x8D, 0xB4, // more data
  0x1C, 0x01, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, // IEND chunk
  0xAE, 0x42, 0x60, 0x82 // IEND CRC
]);

fs.writeFileSync('icon.png', png);
console.log('Created placeholder icon.png (32x32)');
